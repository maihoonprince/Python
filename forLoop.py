# A for loop is a control flow statement in Python that allows you to iterate over a sequence of elements.
# It is commonly used when you have a known number of iterations or when you want to perform a set of operations on each item in a collection.

for i in range(5):
    print(i+1)

a = [1, 34, 456, 76, 459]
for item in a:
    print(item)

print("Printing set......")
s = {23, "prince", 34}
for item in s:
    print(item)

#it is used to many itrable elements...

numbers = [1, 2, 3, 4, 5]
for num in numbers:
    print(num)

# Iterating over a string

message = "Hello, World!"
for char in message:
    print(char)

# Iterating over a range

for i in range(1, 6):
    print(i)

